          <h1 class="box-title">Apa itu gangguan bipolar?</h1>
          <p>Bipolar disorder atau yang juga dikenal sebagai gangguan bipolar adalah suatu kondisi mental yang menyebabkan terjadinya perubahan mood yang ekstrem. Hal ini membuat orang dengan gangguan bipolar memiliki episode mood yang sangat bahagia (mania) atau sangat sedih (depresi). Sering kali, di antara perubahan keduanya, pasien tetap mengalami kondisi mood yang normal.</p>
          <p>Saat pasien merasa sedih, ia akan merasa tertekan, kehilangan harapan, dan bahkan dapat kehilangan keinginan untuk melakukan kegiatan sehari-hari. Tetapi saat merasa senang, pasien akan merasa sangat bersemangat dan penuh gairah. Perubahan mood tersebut dapat terjadi beberapa kali dalam setahun, atau bahkan seminggu dalam kasus yang lebih parah. Kondisi jiwa ini dapat menyebabkan rusaknya hubungan pribadi, rendahnya motivasi dan produktivitas di tempat kerja, dan yang lebih buruk lagi dapat menyebabkan perasaan ingin melakukan bunuh diri. Oleh karena itu, orang-orang dengan bipolar disorder sangat disarankan untuk menghubungi bantuan medis saat mengalami perubahan mood yang signifikan.</p>
